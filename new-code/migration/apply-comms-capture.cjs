'use strict';
/**
 * apply-comms-capture.cjs
 * Staged ADDITIVE migration — capture hardening (Ankit approved 2026-07-02):
 *   ALT-496  email_log          — durable record of every outbound email
 *   (pre-import) import_batch + import_row — the audit/undo tables importEngine.js
 *                already writes to but which DO NOT EXIST in prod (verified
 *                2026-07-02: engine warns + silently skips). MUST be applied
 *                before the first real gateway import or imports are not undoable.
 *   ALT-498  reassignment_log   — old-owner → new-owner journal on lead reassigns
 *
 * STATUS: STAGED — NOT executed. `node -c` checks syntax; run with `--apply`
 *         ONLY after Ankit sign-off (CLAUDE.md §2). Purely additive (3 new
 *         capture tables + the 2 import-audit tables); no existing object touched.
 *
 * The writers are already coded TOLERANT (warn + continue when a table is
 * missing): importEngine.writeBatchRecord, server.js logEmail, assignment.ts
 * journal. Applying this migration flips them from no-op to durable.
 *
 * IMPORTANT — import_batch / import_row columns are dictated by importEngine.js
 * (writeBatchRecord ~line 308 and undoBatch ~line 430): entity, actor_user_id,
 * filename, total, inserted, updated, skipped, error, status, updated_at; rows:
 * batch_id, row_index, status, record_id, undo_payload, error_msg. Do not
 * rename — the engine is the spec.
 *
 * ── ROLLBACK ──────────────────────────────────────────────────────────────────
 *   DROP TABLE IF EXISTS public.import_row;
 *   DROP TABLE IF EXISTS public.import_batch;
 *   DROP TABLE IF EXISTS public.email_log;
 *   DROP TABLE IF EXISTS public.reassignment_log;
 */

const SQL = `
-- ── ALT-496: email send log ───────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.email_log (
  id           bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  sent_to      varchar(320),
  email_type   varchar(80),            -- template/event name (task_reminder, digest, lead_assigned, ...)
  subject      varchar(500),
  status       varchar(10) NOT NULL,   -- 'sent' | 'failed'
  error        text,
  message_id   varchar(200),
  lead_id      bigint,
  report_id    bigint,
  meeting_id   bigint,
  actor        varchar,
  created_date timestamptz NOT NULL DEFAULT now()
);
COMMENT ON TABLE public.email_log IS
  'ALT-496: every outbound email (success + failure), written by notify-service logEmail().';
CREATE INDEX IF NOT EXISTS idx_email_log_created ON public.email_log(created_date);

-- ── Import audit (importEngine.js writeBatchRecord / undoBatch spec) ──────────
CREATE TABLE IF NOT EXISTS public.import_batch (
  id            bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  entity        varchar(20)  NOT NULL,      -- 'company' | 'contact' | 'lead'
  actor_user_id bigint,
  filename      varchar(300),
  total         int          NOT NULL DEFAULT 0,
  inserted      int          NOT NULL DEFAULT 0,
  updated       int          NOT NULL DEFAULT 0,
  skipped       int          NOT NULL DEFAULT 0,
  error         int          NOT NULL DEFAULT 0,
  status        varchar(12)  NOT NULL,      -- 'done' | 'partial' | 'undone'
  created_at    timestamptz  NOT NULL DEFAULT now(),
  updated_at    timestamptz                 -- set by undoBatch on undo
);
COMMENT ON TABLE public.import_batch IS
  'Import audit header (DEC-14): one row per import run; status=undone after importUndo.';

CREATE TABLE IF NOT EXISTS public.import_row (
  id           bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  batch_id     bigint NOT NULL REFERENCES public.import_batch(id),
  row_index    int    NOT NULL,
  status       varchar(12) NOT NULL,        -- 'inserted' | 'updated' | 'skipped' | 'error'
  record_id    bigint,
  undo_payload jsonb,
  error_msg    text
);
COMMENT ON TABLE public.import_row IS
  'Import audit detail: per-row outcome + undo payload (prior values / inserted pk / report_id).';
CREATE INDEX IF NOT EXISTS idx_import_row_batch ON public.import_row(batch_id);

-- ── ALT-498: lead reassignment journal ────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.reassignment_log (
  id           bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  report_id    bigint,
  lead_id      bigint,
  old_user_id  bigint,                      -- NULL when previous owner unknown
  new_user_id  bigint NOT NULL,
  actor        varchar,
  source       varchar(12) NOT NULL DEFAULT 'single',  -- 'single' | 'bulk' | 'departing'
  created_date timestamptz NOT NULL DEFAULT now()
);
COMMENT ON TABLE public.reassignment_log IS
  'ALT-498: durable old→new owner journal for every lead reassignment (written by data/assignment.ts).';
CREATE INDEX IF NOT EXISTS idx_reassignment_log_lead ON public.reassignment_log(lead_id, created_date);
`;

async function main() {
  require('dotenv').config();
  const { Client } = require('pg');
  const c = new Client({ connectionString: process.env.PG_CONNECTION_STRING });
  await c.connect();
  try {
    await c.query('BEGIN');
    await c.query(SQL);
    await c.query('COMMIT');
    console.log('✅ applied: email_log + import_batch + import_row + reassignment_log');
  } catch (e) {
    await c.query('ROLLBACK');
    console.error('❌ rolled back:', e.message);
    process.exitCode = 1;
  } finally {
    await c.end();
  }
}

if (require.main === module && process.argv.includes('--apply')) {
  main();
} else {
  console.log('STAGED — not applied. Run `node apply-comms-capture.cjs --apply` to execute (after sign-off).');
}
