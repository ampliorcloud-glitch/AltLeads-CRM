'use strict';
/**
 * apply-hungerbox-dnc-feasibility.cjs  (ALT-452 + ALT-453 / HungerBox launch)
 *
 * Creates the DNC + Feasibility tables and their audit-history tables.
 *
 * =====================================================================
 * DEPENDENCY
 * =====================================================================
 * Run AFTER apply-hungerbox-company-sites.cjs (needs company_site.site_id).
 *
 * =====================================================================
 * SCHEMA DESIGN
 * =====================================================================
 *
 * DNC and Feasibility are SEPARATE concerns:
 *   DNC        = compliance/opt-out ("this company or site said do not call us")
 *   Feasibility = business fit ("we can't service this company or site")
 *
 * Each exists at two scopes:
 *   scope='company'  → whole company; site_id IS NULL
 *   scope='site'     → a specific city/site; site_id references company_site
 *
 * public.hb_dnc
 *   One row per active DNC flag (one per company, one per site).
 *   is_active toggles without deleting history.
 *   UNIQUE: (company_id, scope) for company-scope; (site_id, scope) for site-scope
 *           — achieved with partial unique indexes below.
 *
 * public.hb_dnc_history
 *   Immutable audit trail: every mark/unmark action.
 *   old_state / new_state = whether DNC was active before/after.
 *
 * public.hb_feasibility
 *   One row per feasibility override (is_feasible=false = non-feasible).
 *   Absent row = feasible (default). is_feasible=true = explicitly re-marked.
 *   UNIQUE same pattern as hb_dnc.
 *
 * public.hb_feasibility_history
 *   Immutable audit trail: every mark/unmark action.
 *
 * =====================================================================
 * STAGED ONLY — node -c to syntax-check; DO NOT execute against the DB
 * until the owner approves and HUNGERBOX_FEATURES is flipped to true.
 *
 * To apply:
 *   cd new-code/migration
 *   node apply-hungerbox-company-sites.cjs   # must be applied first
 *   node apply-hungerbox-dnc-feasibility.cjs
 */
require('dotenv').config();
const { Pool } = require('pg');

const SQL = `
-- =====================================================================
-- HungerBox — DNC + Feasibility  (ALT-452, ALT-453)
-- =====================================================================

-- Reuse or create the updated_at trigger function from the sites migration.
CREATE OR REPLACE FUNCTION public.touch_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at := now();
  RETURN NEW;
END;
$$;

-- ---------------------------------------------------------------------
-- 1. public.hb_dnc
--    Active DNC flags.  scope IN ('company','site').
--    is_active=false = cleared (kept for FK integrity in history table).
-- ---------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.hb_dnc (
  dnc_id        bigint      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id    bigint      NOT NULL,
  site_id       bigint,                          -- NULL for company scope
  scope         text        NOT NULL,
  reason        text        NOT NULL DEFAULT '',
  marked_by     text        NOT NULL,            -- actor user_id as text
  marked_at     timestamptz NOT NULL DEFAULT now(),
  is_active     boolean     NOT NULL DEFAULT true,

  CONSTRAINT hb_dnc_fk_company
    FOREIGN KEY (company_id) REFERENCES public.company_master (company_id)
    ON DELETE CASCADE,

  CONSTRAINT hb_dnc_fk_site
    FOREIGN KEY (site_id) REFERENCES public.company_site (site_id)
    ON DELETE CASCADE
);

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'hb_dnc_scope_chk'
  ) THEN
    ALTER TABLE public.hb_dnc
      ADD CONSTRAINT hb_dnc_scope_chk
      CHECK (scope IN ('company','site'));
  END IF;

  -- Ensure site_id is present when scope='site' and NULL when scope='company'.
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'hb_dnc_scope_site_chk'
  ) THEN
    ALTER TABLE public.hb_dnc
      ADD CONSTRAINT hb_dnc_scope_site_chk
      CHECK (
        (scope = 'company' AND site_id IS NULL) OR
        (scope = 'site'    AND site_id IS NOT NULL)
      );
  END IF;
END $$;

-- Partial unique indexes enforce "one active DNC row per company (or site)".
-- Using partial indexes rather than a table constraint so we can have multiple
-- inactive historical rows without a uniqueness conflict.
CREATE UNIQUE INDEX IF NOT EXISTS hb_dnc_company_scope_uq
  ON public.hb_dnc (company_id, scope)
  WHERE scope = 'company';

CREATE UNIQUE INDEX IF NOT EXISTS hb_dnc_site_scope_uq
  ON public.hb_dnc (site_id, scope)
  WHERE scope = 'site';

-- ---------------------------------------------------------------------
-- 2. public.hb_dnc_history
--    Immutable audit trail for DNC changes.
-- ---------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.hb_dnc_history (
  history_id    bigint      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id    bigint      NOT NULL,
  site_id       bigint,
  scope         text        NOT NULL,
  changed_by    text        NOT NULL,
  changed_at    timestamptz NOT NULL DEFAULT now(),
  old_state     boolean     NOT NULL,   -- was DNC active before this change?
  new_state     boolean     NOT NULL,   -- is DNC active after this change?
  reason        text        NOT NULL DEFAULT '',

  CONSTRAINT hb_dnc_history_fk_company
    FOREIGN KEY (company_id) REFERENCES public.company_master (company_id)
    ON DELETE CASCADE,

  CONSTRAINT hb_dnc_history_fk_site
    FOREIGN KEY (site_id) REFERENCES public.company_site (site_id)
    ON DELETE SET NULL
);

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'hb_dnc_history_scope_chk'
  ) THEN
    ALTER TABLE public.hb_dnc_history
      ADD CONSTRAINT hb_dnc_history_scope_chk
      CHECK (scope IN ('company','site'));
  END IF;
END $$;

-- ---------------------------------------------------------------------
-- 3. public.hb_feasibility
--    Feasibility overrides.  is_feasible=false = non-feasible.
-- ---------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.hb_feasibility (
  feasibility_id bigint      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id     bigint      NOT NULL,
  site_id        bigint,
  scope          text        NOT NULL,
  is_feasible    boolean     NOT NULL DEFAULT true,
  reason         text        NOT NULL DEFAULT '',
  marked_by      text        NOT NULL,
  marked_at      timestamptz NOT NULL DEFAULT now(),

  CONSTRAINT hb_feasibility_fk_company
    FOREIGN KEY (company_id) REFERENCES public.company_master (company_id)
    ON DELETE CASCADE,

  CONSTRAINT hb_feasibility_fk_site
    FOREIGN KEY (site_id) REFERENCES public.company_site (site_id)
    ON DELETE CASCADE
);

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'hb_feasibility_scope_chk'
  ) THEN
    ALTER TABLE public.hb_feasibility
      ADD CONSTRAINT hb_feasibility_scope_chk
      CHECK (scope IN ('company','site'));
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'hb_feasibility_scope_site_chk'
  ) THEN
    ALTER TABLE public.hb_feasibility
      ADD CONSTRAINT hb_feasibility_scope_site_chk
      CHECK (
        (scope = 'company' AND site_id IS NULL) OR
        (scope = 'site'    AND site_id IS NOT NULL)
      );
  END IF;
END $$;

-- Partial unique indexes: one feasibility row per company or site.
CREATE UNIQUE INDEX IF NOT EXISTS hb_feasibility_company_scope_uq
  ON public.hb_feasibility (company_id, scope)
  WHERE scope = 'company';

CREATE UNIQUE INDEX IF NOT EXISTS hb_feasibility_site_scope_uq
  ON public.hb_feasibility (site_id, scope)
  WHERE scope = 'site';

-- ---------------------------------------------------------------------
-- 4. public.hb_feasibility_history
-- ---------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.hb_feasibility_history (
  history_id    bigint      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id    bigint      NOT NULL,
  site_id       bigint,
  scope         text        NOT NULL,
  changed_by    text        NOT NULL,
  changed_at    timestamptz NOT NULL DEFAULT now(),
  old_feasible  boolean     NOT NULL,
  new_feasible  boolean     NOT NULL,
  reason        text        NOT NULL DEFAULT '',

  CONSTRAINT hb_feasibility_history_fk_company
    FOREIGN KEY (company_id) REFERENCES public.company_master (company_id)
    ON DELETE CASCADE,

  CONSTRAINT hb_feasibility_history_fk_site
    FOREIGN KEY (site_id) REFERENCES public.company_site (site_id)
    ON DELETE SET NULL
);

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'hb_feasibility_history_scope_chk'
  ) THEN
    ALTER TABLE public.hb_feasibility_history
      ADD CONSTRAINT hb_feasibility_history_scope_chk
      CHECK (scope IN ('company','site'));
  END IF;
END $$;

-- ---------------------------------------------------------------------
-- 5. Indexes
-- ---------------------------------------------------------------------
-- DNC: look up all DNC companies fast (work-queue filter)
CREATE INDEX IF NOT EXISTS hb_dnc_active_company_idx
  ON public.hb_dnc (company_id)
  WHERE is_active = true AND scope = 'company';

CREATE INDEX IF NOT EXISTS hb_dnc_active_site_idx
  ON public.hb_dnc (site_id)
  WHERE is_active = true AND scope = 'site' AND site_id IS NOT NULL;

-- Feasibility: look up all non-feasible companies fast
CREATE INDEX IF NOT EXISTS hb_feasibility_nonfeasible_company_idx
  ON public.hb_feasibility (company_id)
  WHERE is_feasible = false AND scope = 'company';

CREATE INDEX IF NOT EXISTS hb_feasibility_nonfeasible_site_idx
  ON public.hb_feasibility (site_id)
  WHERE is_feasible = false AND scope = 'site' AND site_id IS NOT NULL;

-- History: newest-first per company
CREATE INDEX IF NOT EXISTS hb_dnc_history_company_changed_at_idx
  ON public.hb_dnc_history (company_id, changed_at DESC);

CREATE INDEX IF NOT EXISTS hb_feasibility_history_company_changed_at_idx
  ON public.hb_feasibility_history (company_id, changed_at DESC);

-- ---------------------------------------------------------------------
-- 6. GRANTS
-- ---------------------------------------------------------------------
REVOKE ALL ON public.hb_dnc                 FROM anon;
REVOKE ALL ON public.hb_dnc                 FROM PUBLIC;
REVOKE ALL ON public.hb_dnc_history         FROM anon;
REVOKE ALL ON public.hb_dnc_history         FROM PUBLIC;
REVOKE ALL ON public.hb_feasibility         FROM anon;
REVOKE ALL ON public.hb_feasibility         FROM PUBLIC;
REVOKE ALL ON public.hb_feasibility_history FROM anon;
REVOKE ALL ON public.hb_feasibility_history FROM PUBLIC;

GRANT SELECT, INSERT, UPDATE ON public.hb_dnc                 TO authenticated;
GRANT SELECT, INSERT          ON public.hb_dnc_history         TO authenticated;
GRANT SELECT, INSERT, UPDATE ON public.hb_feasibility         TO authenticated;
GRANT SELECT, INSERT          ON public.hb_feasibility_history TO authenticated;

GRANT USAGE ON SEQUENCE public.hb_dnc_dnc_id_seq                         TO authenticated;
GRANT USAGE ON SEQUENCE public.hb_dnc_history_history_id_seq              TO authenticated;
GRANT USAGE ON SEQUENCE public.hb_feasibility_feasibility_id_seq          TO authenticated;
GRANT USAGE ON SEQUENCE public.hb_feasibility_history_history_id_seq      TO authenticated;
`;

(async () => {
  const pg = new Pool({ connectionString: process.env.PG_CONNECTION_STRING });
  const client = await pg.connect();
  try {
    await client.query('BEGIN');
    await client.query(SQL);
    await client.query('COMMIT');
    console.log('APPLIED apply-hungerbox-dnc-feasibility OK (committed).');
  } catch (e) {
    await client.query('ROLLBACK');
    console.error('APPLY FAILED, rolled back:', e.message);
    client.release();
    await pg.end();
    process.exit(1);
  }

  // === VERIFY ===
  const tables = ['hb_dnc', 'hb_dnc_history', 'hb_feasibility', 'hb_feasibility_history'];
  for (const tbl of tables) {
    const cols = await client.query(`
      SELECT column_name, data_type, is_nullable
      FROM information_schema.columns
      WHERE table_schema='public' AND table_name=$1
      ORDER BY ordinal_position
    `, [tbl]);
    console.log(`\n=== ${tbl} (${cols.rows.length} cols) ===`);
    cols.rows.forEach(r => console.log(`  ${r.column_name}: ${r.data_type}${r.is_nullable==='NO'?' NOT NULL':''}`));
  }

  const idx = await client.query(`
    SELECT indexname FROM pg_indexes
    WHERE schemaname='public'
      AND tablename = ANY($1::text[])
    ORDER BY indexname
  `, [tables]);
  console.log('\n=== INDEXES ===');
  console.log(JSON.stringify(idx.rows.map(r => r.indexname), null, 2));

  const grants = await client.query(`
    SELECT table_name, grantee, privilege_type
    FROM information_schema.role_table_grants
    WHERE table_schema='public'
      AND table_name = ANY($1::text[])
      AND grantee IN ('anon','authenticated','PUBLIC')
    ORDER BY table_name, grantee, privilege_type
  `, [tables]);
  console.log('\n=== GRANTS ===');
  console.log(JSON.stringify(grants.rows, null, 2));

  client.release();
  await pg.end();
})().catch(e => { console.error('ERR:', e.message); process.exit(1); });
