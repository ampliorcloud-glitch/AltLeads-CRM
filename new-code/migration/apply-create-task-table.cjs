'use strict';
/**
 * apply-create-task-table.cjs — Task Manager module, DB slice 1 (ALT-160 / ALT-209).
 *
 * Creates public.task + public.task_user_pref per the canonical Task data
 * contract in docs/product/TASK-MANAGER.md, plus:
 *   - a BEFORE INSERT/UPDATE trigger that maintains reminder_at
 *     (= due_at - remind_offset_minutes) and clears reminder_sent_at whenever
 *     due_at or remind_offset_minutes changes (so snooze/reschedule re-fires);
 *   - a touch-updated_date trigger;
 *   - a PARTIAL index for the reminder scanner's "what's due?" query;
 *   - the My-Tasks list index.
 *
 * Self-contained & idempotent: the SQL is embedded below as a template string
 * (raw *.sql is gitignored, so the tracked applier must be reproducible on its
 * own). CREATE ... IF NOT EXISTS / CREATE OR REPLACE / DROP TRIGGER IF EXISTS /
 * DO-block guards make re-running safe.
 *
 * Mirrors new-code/migration/apply-access-rls.js: single BEGIN/COMMIT
 * transaction, ROLLBACK on error, then VERIFY queries that print AFTER state.
 *
 * RUN LATER (do not run automatically):  node apply-create-task-table.cjs
 * RLS is applied separately by apply-task-rls.cjs (run it AFTER this).
 */
require('dotenv').config();
const { Pool } = require('pg');

const SQL = `
-- =====================================================================
-- TASK MANAGER — schema slice 1: public.task + public.task_user_pref
-- =====================================================================

-- ---------------------------------------------------------------------
-- 1. public.task
-- ---------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.task (
  task_id                bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  task_type              text        NOT NULL,
  subject                text        NOT NULL,
  body                   text,
  status                 text        NOT NULL DEFAULT 'OPEN',
  priority               text        NOT NULL DEFAULT 'NORMAL',
  owner_user_id          bigint      NOT NULL,
  assigned_by_user_id    bigint,
  lead_id                bigint,
  company_id             bigint,
  contact_id             bigint,
  meeting_id             bigint,
  assoc_label            text,
  assoc_phone            text,
  due_at                 timestamptz NOT NULL,
  remind_offset_minutes  integer     NOT NULL DEFAULT 0,
  reminder_at            timestamptz,            -- trigger-maintained
  reminder_sent_at       timestamptz,
  created_by             bigint,
  created_date           timestamptz NOT NULL DEFAULT now(),
  updated_date           timestamptz,
  deleted_date           timestamptz
);

-- CHECK constraints (guarded so re-runs don't error on duplicate name) ----
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'task_task_type_chk'
  ) THEN
    ALTER TABLE public.task
      ADD CONSTRAINT task_task_type_chk
      CHECK (task_type IN ('CALL','MEETING','TODO'));
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'task_status_chk'
  ) THEN
    ALTER TABLE public.task
      ADD CONSTRAINT task_status_chk
      CHECK (status IN ('OPEN','DONE','SKIPPED'));
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'task_priority_chk'
  ) THEN
    ALTER TABLE public.task
      ADD CONSTRAINT task_priority_chk
      CHECK (priority IN ('LOW','NORMAL','HIGH'));
  END IF;
END $$;

-- ---------------------------------------------------------------------
-- 2. public.task_user_pref  (Decision B: daily digest opt-in, DEFAULT false)
-- ---------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.task_user_pref (
  user_id              bigint      PRIMARY KEY,
  daily_digest_opt_in  boolean     NOT NULL DEFAULT false,
  created_date         timestamptz NOT NULL DEFAULT now(),
  updated_date         timestamptz
);

-- ---------------------------------------------------------------------
-- 3. Trigger fn: maintain reminder_at + clear reminder_sent_at
--    On INSERT, and on any UPDATE that changes due_at or
--    remind_offset_minutes: recompute reminder_at = due_at - offset and
--    reset reminder_sent_at to NULL so the scanner can fire it again
--    (snooze/reschedule flows through here — TASK-MANAGER.md §6.4).
-- ---------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.task_maintain_reminder()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  IF TG_OP = 'INSERT'
     OR NEW.due_at IS DISTINCT FROM OLD.due_at
     OR NEW.remind_offset_minutes IS DISTINCT FROM OLD.remind_offset_minutes
  THEN
    NEW.reminder_at :=
      NEW.due_at - (NEW.remind_offset_minutes || ' minutes')::interval;
    NEW.reminder_sent_at := NULL;
  END IF;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS task_maintain_reminder_trg ON public.task;
CREATE TRIGGER task_maintain_reminder_trg
  BEFORE INSERT OR UPDATE ON public.task
  FOR EACH ROW
  EXECUTE FUNCTION public.task_maintain_reminder();

-- ---------------------------------------------------------------------
-- 4. Trigger fn: touch updated_date on UPDATE (task + task_user_pref)
-- ---------------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.touch_updated_date()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_date := now();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS task_touch_updated_date_trg ON public.task;
CREATE TRIGGER task_touch_updated_date_trg
  BEFORE UPDATE ON public.task
  FOR EACH ROW
  EXECUTE FUNCTION public.touch_updated_date();

DROP TRIGGER IF EXISTS task_user_pref_touch_updated_date_trg ON public.task_user_pref;
CREATE TRIGGER task_user_pref_touch_updated_date_trg
  BEFORE UPDATE ON public.task_user_pref
  FOR EACH ROW
  EXECUTE FUNCTION public.touch_updated_date();

-- ---------------------------------------------------------------------
-- 5. Indexes
-- ---------------------------------------------------------------------
-- My-Tasks list views (owner + status + due ordering)
CREATE INDEX IF NOT EXISTS task_owner_status_due_idx
  ON public.task (owner_user_id, status, due_at);

-- Scanner "what's due?" partial index — matches the scanner WHERE exactly
CREATE INDEX IF NOT EXISTS task_reminder_scan_idx
  ON public.task (reminder_at)
  WHERE status = 'OPEN'
    AND reminder_at IS NOT NULL
    AND reminder_sent_at IS NULL
    AND deleted_date IS NULL;

-- FK / association lookup indexes (open tasks on a record)
CREATE INDEX IF NOT EXISTS task_lead_id_idx    ON public.task (lead_id)    WHERE lead_id    IS NOT NULL;
CREATE INDEX IF NOT EXISTS task_company_id_idx ON public.task (company_id) WHERE company_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS task_contact_id_idx ON public.task (contact_id) WHERE contact_id IS NOT NULL;

-- ---------------------------------------------------------------------
-- 6. GRANTS — explicit-grant discipline (mirror apply-portal-foundation.cjs).
--    Supabase ships broad default schema grants to anon+authenticated; make
--    the posture explicit so RLS is NOT the sole barrier:
--      * REVOKE ALL from anon (anon must never hold table privileges), and
--      * GRANT only the intended DML to authenticated.
--    RLS then scopes WHICH rows an authenticated caller may touch.
-- ---------------------------------------------------------------------
REVOKE ALL ON public.task           FROM anon;
REVOKE ALL ON public.task_user_pref FROM anon;
REVOKE ALL ON public.task           FROM PUBLIC;
REVOKE ALL ON public.task_user_pref FROM PUBLIC;

GRANT SELECT, INSERT, UPDATE, DELETE ON public.task           TO authenticated;
GRANT SELECT, INSERT, UPDATE, DELETE ON public.task_user_pref TO authenticated;
`;

