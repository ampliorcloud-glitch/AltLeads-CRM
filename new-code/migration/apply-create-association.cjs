'use strict';
/**
 * apply-create-association.cjs — Associations feature, DB slice 1 (ALT-442).
 *
 * ============================ DO NOT AUTO-RUN ============================
 * This is a STAGED migration. Launch posture = MANUAL deploys. It must be run
 * by hand AFTER owner sign-off + throwaway-login validation (an admin login
 * verifies the grants; a non-admin login is scoped by the RLS applier below).
 * Show the owner first. Running it in production without that gate is forbidden.
 * Nothing in the build pipeline invokes this file.
 * ========================================================================
 *
 * Creates public.record_association: the table that models v1 associations
 * between CRM records (Lead ↔ Contact in Phase 1; open to Company / Meeting
 * in later phases). Each row links a (from_type, from_id) to a (to_type,
 * to_id) with an optional label (one of 5 built-in labels: 'Decision Maker',
 * 'Influencer', 'Technical Contact', 'Finance Contact', 'Other') and an
 * is_primary flag to mark the primary contact for a lead.
 *
 * DATA-LAYER CONVENTION (no-mirror guarantee):
 *   The application layer (data/associations.ts) MUST normalise endpoint
 *   ordering before writing: whichever (type, id) pair sorts lower
 *   lexicographically (type first, then id) is always stored as (from_type,
 *   from_id). This prevents mirror-duplicate rows like
 *     (lead, 1) ↔ (contact, 5)  AND  (contact, 5) ↔ (lead, 1)
 *   existing at the same time. The PARTIAL UNIQUE index on
 *   (from_type, from_id, to_type, to_id) WHERE deleted_date IS NULL enforces
 *   uniqueness of the normalised pair.
 *
 * NOTE: RLS (row-level security) is intentionally NOT applied here. It is a
 * SEPARATE later applier gated on DEC-03 ownership-model validation. The
 * table is safe to create first: table grants already block anon/PUBLIC; RLS
 * will scope authenticated callers to their relevant records once that
 * applier is applied.
 *
 * USAGE:  node apply-create-association.cjs             (apply, staged/manual)
 *         node apply-create-association.cjs --rollback  (DROP the table)
 *
 * Mirrors apply-create-task-table.cjs / apply-merge-rpc.cjs: require('dotenv'),
 * new Pool({ connectionString }), single BEGIN/COMMIT, ROLLBACK on error,
 * idempotent CREATE TABLE IF NOT EXISTS, DO-block guards for constraints,
 * explicit grants, VERIFY queries printed after, --rollback branch.
 */
require('dotenv').config();
const { Pool } = require('pg');

const SQL = `
-- =====================================================================
-- ALT-442 — ASSOCIATIONS v1: public.record_association
-- =====================================================================

-- ---------------------------------------------------------------------
-- 1. Table
-- ---------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.record_association (
  association_id  bigint      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

  -- "From" endpoint (always the lexicographically-lower (type, id) pair —
  -- see the data-layer convention note in the applier header above)
  from_type       text        NOT NULL,
  from_id         bigint      NOT NULL,

  -- "To" endpoint
  to_type         text        NOT NULL,
  to_id           bigint      NOT NULL,

  -- v1 label (one of 5 built-in values, or NULL when no label is needed)
  -- Built-in labels: 'Decision Maker', 'Influencer', 'Technical Contact',
  --                  'Finance Contact', 'Other'
  assoc_label     text        NULL,

  -- Marks the primary contact for a lead (only meaningful on Lead↔Contact links)
  is_primary      boolean     NOT NULL DEFAULT false,

  -- Optional project scope
  project_id      bigint      NULL,

  -- Audit
  created_by      text,
  created_date    timestamptz NOT NULL DEFAULT now(),
  updated_by      text,
  updated_date    timestamptz,

  -- Soft-delete
  deleted_by      text,
  deleted_date    timestamptz
);

-- ---------------------------------------------------------------------
-- 2. CHECK constraints (DO-guarded so re-runs don't error on duplicate name)
-- ---------------------------------------------------------------------
DO $$
BEGIN
  -- from_type must be one of the 4 supported record types
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'record_association_from_type_chk'
  ) THEN
    ALTER TABLE public.record_association
      ADD CONSTRAINT record_association_from_type_chk
      CHECK (from_type IN ('contact','company','lead','meeting'));
  END IF;

  -- to_type must be one of the 4 supported record types
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'record_association_to_type_chk'
  ) THEN
    ALTER TABLE public.record_association
      ADD CONSTRAINT record_association_to_type_chk
      CHECK (to_type IN ('contact','company','lead','meeting'));
  END IF;

  -- A record must not be associated with itself
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'record_association_no_self_chk'
  ) THEN
    ALTER TABLE public.record_association
      ADD CONSTRAINT record_association_no_self_chk
      CHECK (NOT (from_type = to_type AND from_id = to_id));
  END IF;

  -- assoc_label, when provided, must be one of the 5 built-in v1 labels
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'record_association_label_chk'
  ) THEN
    ALTER TABLE public.record_association
      ADD CONSTRAINT record_association_label_chk
      CHECK (
        assoc_label IS NULL
        OR assoc_label IN (
          'Decision Maker',
          'Influencer',
          'Technical Contact',
          'Finance Contact',
          'Other'
        )
      );
  END IF;
END $$;

-- ---------------------------------------------------------------------
-- 3. Indexes
-- ---------------------------------------------------------------------

-- Forward lookup: all active associations originating from a given record
CREATE INDEX IF NOT EXISTS record_association_from_idx
  ON public.record_association (from_type, from_id)
  WHERE deleted_date IS NULL;

-- Reverse lookup: all active associations pointing to a given record
CREATE INDEX IF NOT EXISTS record_association_to_idx
  ON public.record_association (to_type, to_id)
  WHERE deleted_date IS NULL;

-- Uniqueness: one live link per normalised (from, to) pair.
-- Mirror duplicates are prevented by the data-layer ordering convention;
-- this index enforces it at the DB level for active rows.
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_indexes
     WHERE schemaname = 'public'
       AND tablename  = 'record_association'
       AND indexname  = 'record_association_unique_active_idx'
  ) THEN
    CREATE UNIQUE INDEX record_association_unique_active_idx
      ON public.record_association (from_type, from_id, to_type, to_id)
      WHERE deleted_date IS NULL;
  END IF;
END $$;

-- ---------------------------------------------------------------------
-- 4. GRANTS — explicit-grant discipline (mirror apply-create-task-table.cjs).
--    Supabase ships broad default schema grants to anon+authenticated; make
--    the posture explicit so RLS is NOT the sole barrier:
--      * REVOKE ALL from anon/PUBLIC (anon must never hold table privileges),
--      * GRANT only the intended DML to authenticated.
--    The separate RLS applier (ALT-442, gated on DEC-03) then scopes WHICH
--    rows an authenticated caller may touch.
-- ---------------------------------------------------------------------
REVOKE ALL ON public.record_association FROM anon;
REVOKE ALL ON public.record_association FROM PUBLIC;

GRANT SELECT, INSERT, UPDATE, DELETE ON public.record_association TO authenticated;
`;

// Drops the table. Use if validation fails or to fully revert.
const ROLLBACK_SQL = `
DROP TABLE IF EXISTS public.record_association;
`;

(async () => {
  const rollback = process.argv.includes('--rollback');
  const pg = new Pool({ connectionString: process.env.PG_CONNECTION_STRING });
  const client = await pg.connect();
  try {
    await client.query('BEGIN');
    await client.query(rollback ? ROLLBACK_SQL : SQL);
    await client.query('COMMIT');
    console.log(rollback
      ? 'ROLLED BACK create-association OK (committed).'
      : 'APPLIED create-association OK (committed).');
  } catch (e) {
    await client.query('ROLLBACK');
    console.error('OPERATION FAILED, rolled back:', e.message);
    client.release();
    await pg.end();
    process.exit(1);
  }

  // === VERIFY: columns of public.record_association ===
  const cols = await client.query(
    `SELECT column_name, data_type, is_nullable, column_default
     FROM information_schema.columns
     WHERE table_schema = 'public' AND table_name = 'record_association'
     ORDER BY ordinal_position`);
  console.log('\n=== public.record_association COLUMNS (AFTER) ===');
  console.log(JSON.stringify(cols.rows, null, 2));

  // === VERIFY: CHECK constraints ===
  const cons = await client.query(
    `SELECT conname, pg_get_constraintdef(oid) AS def
     FROM pg_constraint
     WHERE conrelid = 'public.record_association'::regclass AND contype = 'c'
     ORDER BY conname`);
  console.log('\n=== public.record_association CHECK CONSTRAINTS (AFTER) ===');
  console.log(JSON.stringify(cons.rows, null, 2));

  // === VERIFY: indexes ===
  const idx = await client.query(
    `SELECT indexname, indexdef
     FROM pg_indexes
     WHERE schemaname = 'public' AND tablename = 'record_association'
     ORDER BY indexname`);
  console.log('\n=== public.record_association INDEXES (AFTER) ===');
  console.log(JSON.stringify(idx.rows, null, 2));

  // === VERIFY: table grants (anon/PUBLIC must hold NONE; authenticated holds DML) ===
  const grants = await client.query(
    `SELECT table_name, grantee, privilege_type
     FROM information_schema.role_table_grants
     WHERE table_schema = 'public'
       AND table_name   = 'record_association'
       AND grantee IN ('anon','authenticated','PUBLIC')
     ORDER BY grantee, privilege_type`);
  console.log('\n=== public.record_association TABLE GRANTS (AFTER) ===');
  console.log(JSON.stringify(grants.rows, null, 2));

  client.release();
  await pg.end();
})().catch(e => { console.error('ERR:', e.message); process.exit(1); });
