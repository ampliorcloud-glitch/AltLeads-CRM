'use strict';
/**
 * apply-leadstate-qualification-lost-utm.cjs
 * Staged ADDITIVE migration — ALT-470 (UTM attribution) + ALT-471 (qualification
 * status + audit) + ALT-472 (structured lost-reason capture).
 *
 * STATUS: STAGED — NOT executed against any database.
 *         `node -c apply-leadstate-qualification-lost-utm.cjs` syntax-checks only.
 *         Apply to prod ONLY after Ankit's go-ahead (CLAUDE.md §2). It is purely
 *         additive (new nullable columns + two new lookup/junction tables) so it
 *         is safe + reversible, but we still stage it and flip the matching UI
 *         flag (LEAD_STATE_V2) only after apply.
 *
 * WHY THIS EXISTS (verified via introspection 2026-06-30):
 *   This is a meeting-outreach CRM. `stage_master` holds only meeting-outcome
 *   stages (Meeting Scheduled / Successful / Cancelled / Postponed …). There is
 *   NO qualification concept and NO structured "why we lost" capture; the
 *   `status_master` table is empty. The open-source CRM study (ERPNext) flagged
 *   both as P1 gaps (ALT-471 / ALT-472). UTM attribution (ALT-470) is also absent
 *   — `lead_master.source_id` is a coarse FK source; UTM is finer-grained.
 *
 * MODEL DECISIONS (match existing schema conventions):
 *   • Qualification hangs off `lead_report` (the per-PROJECT lead-state record),
 *     not `lead_master` — qualification is a per-client judgement, like ownership.
 *   • `qualified_by` is BIGINT (the app's `user_id` convention, same as
 *     lead_report.user_id), NOT the uuid profiles.id.
 *   • Lost reasons are a lookup + junction keyed on `report_id` (per-project state).
 *   • UTM columns live on `lead_master` (the lead identity), populated at import.
 *
 * IDEMPOTENT: every statement uses IF NOT EXISTS. Safe to re-run.
 *
 * RELEVANT TICKETS: ALT-470, ALT-471, ALT-472.
 * RELATED: PROJECT-READ-ISOLATION (lead_report / lead_master already RLS-scoped);
 *          the new tables get the same membership-gated SELECT when that RLS applies.
 *
 * ── ROLLBACK (manual, if ever needed) ────────────────────────────────────────
 *   ALTER TABLE lead_report  DROP COLUMN IF EXISTS qualification_status, qualified_by, qualified_on;
 *   ALTER TABLE lead_master  DROP COLUMN IF EXISTS utm_source, utm_medium, utm_campaign;
 *   DROP TABLE IF EXISTS lead_lost_reason;
 *   DROP TABLE IF EXISTS lost_reason;
 */

const SQL = `
-- ════════════════════════════════════════════════════════════════════════════
-- ALT-471 — Qualification status (Unqualified / In Process / Qualified) + audit
-- ════════════════════════════════════════════════════════════════════════════
ALTER TABLE public.lead_report
  ADD COLUMN IF NOT EXISTS qualification_status varchar(20),     -- NULL = not yet triaged
  ADD COLUMN IF NOT EXISTS qualified_by         bigint,          -- app user_id of the QC/agent who set it
  ADD COLUMN IF NOT EXISTS qualified_on         timestamptz;

COMMENT ON COLUMN public.lead_report.qualification_status IS
  'ALT-471: unqualified | in_process | qualified. Separate from report_status/stage. NULL=not triaged.';
COMMENT ON COLUMN public.lead_report.qualified_by IS
  'ALT-471: app user_id (bigint, matches lead_report.user_id convention) who last set qualification_status.';
COMMENT ON COLUMN public.lead_report.qualified_on IS
  'ALT-471: timestamp qualification_status was last set.';

-- Soft guard on the allowed value set (NULL always allowed). Additive, no backfill.
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'lead_report_qualification_status_chk'
  ) THEN
    ALTER TABLE public.lead_report
      ADD CONSTRAINT lead_report_qualification_status_chk
      CHECK (qualification_status IS NULL
             OR qualification_status IN ('unqualified','in_process','qualified'));
  END IF;
END $$;

-- ════════════════════════════════════════════════════════════════════════════
-- ALT-470 — UTM / lead attribution (populated at import; finer than source_id)
-- ════════════════════════════════════════════════════════════════════════════
ALTER TABLE public.lead_master
  ADD COLUMN IF NOT EXISTS utm_source   varchar(120),
  ADD COLUMN IF NOT EXISTS utm_medium   varchar(120),
  ADD COLUMN IF NOT EXISTS utm_campaign varchar(160);

COMMENT ON COLUMN public.lead_master.utm_source   IS 'ALT-470: campaign source (e.g. linkedin, webinar). Maps at import.';
COMMENT ON COLUMN public.lead_master.utm_medium   IS 'ALT-470: campaign medium (e.g. cold-email, paid). Maps at import.';
COMMENT ON COLUMN public.lead_master.utm_campaign IS 'ALT-470: campaign name. Maps at import.';

-- ════════════════════════════════════════════════════════════════════════════
-- ALT-472 — Structured lost-reason capture (lookup + junction)
-- ════════════════════════════════════════════════════════════════════════════
CREATE TABLE IF NOT EXISTS public.lost_reason (
  lost_reason_id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  label          varchar(120) NOT NULL,
  is_active      boolean      NOT NULL DEFAULT true,
  sort_order     int          NOT NULL DEFAULT 0,
  created_by     varchar,
  created_date   timestamptz  NOT NULL DEFAULT now(),
  updated_by     varchar,
  updated_date   timestamptz,
  deleted_by     varchar,
  deleted_date   timestamptz
);
COMMENT ON TABLE public.lost_reason IS
  'ALT-472: admin-editable lookup of structured reasons a lead is lost/cancelled/dropped.';

-- Junction: per-project lead-state (report) ↔ reason. UI requires ≥1 when the
-- stage moves to a cancelled/dropped state. (DB-level enforcement is deferred to
-- a future trigger — kept presentational/UI-first to stay additive.)
CREATE TABLE IF NOT EXISTS public.lead_lost_reason (
  id             bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  report_id      bigint NOT NULL REFERENCES public.lead_report(report_id),
  lost_reason_id bigint NOT NULL REFERENCES public.lost_reason(lost_reason_id),
  notes          text,
  created_by     varchar,
  created_date   timestamptz NOT NULL DEFAULT now(),
  deleted_by     varchar,
  deleted_date   timestamptz
);
COMMENT ON TABLE public.lead_lost_reason IS
  'ALT-472: junction lead_report.report_id ↔ lost_reason. Multi-select per lost lead.';

CREATE INDEX IF NOT EXISTS idx_lead_lost_reason_report ON public.lead_lost_reason(report_id);
-- one live row per (report, reason)
CREATE UNIQUE INDEX IF NOT EXISTS uq_lead_lost_reason_live
  ON public.lead_lost_reason(report_id, lost_reason_id)
  WHERE deleted_date IS NULL;

-- Starter reason set (generic + admin-editable later). Seeded only if table empty.
INSERT INTO public.lost_reason (label, sort_order, created_by)
SELECT v.label, v.ord, 'migration'
FROM (VALUES
  ('No budget / cost',            10),
  ('No current need',             20),
  ('Chose a competitor',          30),
  ('Bad timing / revisit later',  40),
  ('Unresponsive / no show',      50),
  ('Not a fit (size / segment)',  60),
  ('Wrong contact / gatekeeper',  70),
  ('Duplicate / bad data',        80),
  ('Other',                       99)
) AS v(label, ord)
WHERE NOT EXISTS (SELECT 1 FROM public.lost_reason);
`;

async function main() {
  require('dotenv').config();
  const { Client } = require('pg');
  const c = new Client({ connectionString: process.env.PG_CONNECTION_STRING });
  await c.connect();
  try {
    await c.query('BEGIN');
    await c.query(SQL);
    await c.query('COMMIT');
    console.log('✅ applied: ALT-470/471/472 additive lead-state fields + lost_reason tables');
  } catch (e) {
    await c.query('ROLLBACK');
    console.error('❌ rolled back:', e.message);
    process.exitCode = 1;
  } finally {
    await c.end();
  }
}

// Guard: never auto-run on require. Execute deliberately with `node <file> --apply`.
if (require.main === module && process.argv.includes('--apply')) {
  main();
} else {
  console.log('STAGED — not applied. Run `node apply-leadstate-qualification-lost-utm.cjs --apply` to execute (after sign-off).');
}
