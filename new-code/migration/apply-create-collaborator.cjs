'use strict';
/**
 * apply-create-collaborator.cjs — Collaborators feature, DB slice 1 (ALT-441).
 *
 * ============================ DO NOT AUTO-RUN ============================
 * This is a STAGED migration. Launch posture = MANUAL deploys. It must be run
 * by hand AFTER owner sign-off + throwaway-login validation (an admin login
 * verifies the grants; a non-admin login is scoped by the RLS applier below).
 * Show the owner first. Running it in production without that gate is forbidden.
 * Nothing in the build pipeline invokes this file.
 * ========================================================================
 *
 * Creates public.record_collaborator: the junction table that tracks WHICH
 * internal users are collaborating on a given record (contact / company /
 * lead / meeting). Collaborators are internal CRM users (user_id in profiles);
 * they are kept strictly separate from record associations (see
 * apply-create-association.cjs / ALT-442).
 *
 * NOTE: RLS (row-level security) is intentionally NOT applied here. It is a
 * SEPARATE later applier gated on DEC-03 ownership-model validation. The
 * table is safe to create first: table grants already block anon/PUBLIC; RLS
 * will scope authenticated callers to their own assignments once that applier
 * is applied.
 *
 * USAGE:  node apply-create-collaborator.cjs             (apply, staged/manual)
 *         node apply-create-collaborator.cjs --rollback  (DROP the table)
 *
 * Mirrors apply-create-task-table.cjs / apply-merge-rpc.cjs: require('dotenv'),
 * new Pool({ connectionString }), single BEGIN/COMMIT, ROLLBACK on error,
 * idempotent CREATE TABLE IF NOT EXISTS, DO-block guards for constraints,
 * explicit grants, VERIFY queries printed after, --rollback branch.
 */
require('dotenv').config();
const { Pool } = require('pg');

const SQL = `
-- =====================================================================
-- ALT-441 — COLLABORATORS: public.record_collaborator
-- =====================================================================

-- ---------------------------------------------------------------------
-- 1. Table
-- ---------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.record_collaborator (
  collaborator_id   bigint      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

  -- Which record this collaborator is attached to
  record_type       text        NOT NULL,
  record_id         bigint      NOT NULL,

  -- Optional project scope (NULL = record-level only, no project scope)
  project_id        bigint      NULL,

  -- The internal CRM user who is the collaborator
  user_id           bigint      NOT NULL,

  -- Role label (defaults to 'collaborator'; open-ended for future extension)
  collaborator_role text        NULL DEFAULT 'collaborator',

  -- Audit
  created_by        text,
  created_date      timestamptz NOT NULL DEFAULT now(),
  updated_by        text,
  updated_date      timestamptz,

  -- Soft-delete
  deleted_by        text,
  deleted_date      timestamptz
);

-- ---------------------------------------------------------------------
-- 2. CHECK constraints (DO-guarded so re-runs don't error on duplicate name)
-- ---------------------------------------------------------------------
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint WHERE conname = 'record_collaborator_record_type_chk'
  ) THEN
    ALTER TABLE public.record_collaborator
      ADD CONSTRAINT record_collaborator_record_type_chk
      CHECK (record_type IN ('contact','company','lead','meeting'));
  END IF;
END $$;

-- ---------------------------------------------------------------------
-- 3. Indexes
-- ---------------------------------------------------------------------

-- Primary lookup: all active collaborators on a given record
CREATE INDEX IF NOT EXISTS record_collaborator_record_idx
  ON public.record_collaborator (record_type, record_id)
  WHERE deleted_date IS NULL;

-- Reverse lookup: all active records a given user is collaborating on
CREATE INDEX IF NOT EXISTS record_collaborator_user_idx
  ON public.record_collaborator (user_id)
  WHERE deleted_date IS NULL;

-- Uniqueness: one user may appear at most once per live (record_type, record_id) pair.
-- Using a PARTIAL unique index so soft-deleted rows don't block re-adding.
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_indexes
     WHERE schemaname = 'public'
       AND tablename  = 'record_collaborator'
       AND indexname  = 'record_collaborator_unique_active_idx'
  ) THEN
    CREATE UNIQUE INDEX record_collaborator_unique_active_idx
      ON public.record_collaborator (record_type, record_id, user_id)
      WHERE deleted_date IS NULL;
  END IF;
END $$;

-- ---------------------------------------------------------------------
-- 4. GRANTS — explicit-grant discipline (mirror apply-create-task-table.cjs).
--    Supabase ships broad default schema grants to anon+authenticated; make
--    the posture explicit so RLS is NOT the sole barrier:
--      * REVOKE ALL from anon/PUBLIC (anon must never hold table privileges),
--      * GRANT only the intended DML to authenticated.
--    The separate RLS applier (ALT-441, gated on DEC-03) then scopes WHICH
--    rows an authenticated caller may touch.
-- ---------------------------------------------------------------------
REVOKE ALL ON public.record_collaborator FROM anon;
REVOKE ALL ON public.record_collaborator FROM PUBLIC;

GRANT SELECT, INSERT, UPDATE, DELETE ON public.record_collaborator TO authenticated;
`;

// Drops the table. Use if validation fails or to fully revert.
const ROLLBACK_SQL = `
DROP TABLE IF EXISTS public.record_collaborator;
`;

(async () => {
  const rollback = process.argv.includes('--rollback');
  const pg = new Pool({ connectionString: process.env.PG_CONNECTION_STRING });
  const client = await pg.connect();
  try {
    await client.query('BEGIN');
    await client.query(rollback ? ROLLBACK_SQL : SQL);
    await client.query('COMMIT');
    console.log(rollback
      ? 'ROLLED BACK create-collaborator OK (committed).'
      : 'APPLIED create-collaborator OK (committed).');
  } catch (e) {
    await client.query('ROLLBACK');
    console.error('OPERATION FAILED, rolled back:', e.message);
    client.release();
    await pg.end();
    process.exit(1);
  }

  // === VERIFY: columns of public.record_collaborator ===
  const cols = await client.query(
    `SELECT column_name, data_type, is_nullable, column_default
     FROM information_schema.columns
     WHERE table_schema = 'public' AND table_name = 'record_collaborator'
     ORDER BY ordinal_position`);
  console.log('\n=== public.record_collaborator COLUMNS (AFTER) ===');
  console.log(JSON.stringify(cols.rows, null, 2));

  // === VERIFY: CHECK constraints ===
  const cons = await client.query(
    `SELECT conname, pg_get_constraintdef(oid) AS def
     FROM pg_constraint
     WHERE conrelid = 'public.record_collaborator'::regclass AND contype = 'c'
     ORDER BY conname`);
  console.log('\n=== public.record_collaborator CHECK CONSTRAINTS (AFTER) ===');
  console.log(JSON.stringify(cons.rows, null, 2));

  // === VERIFY: indexes ===
  const idx = await client.query(
    `SELECT indexname, indexdef
     FROM pg_indexes
     WHERE schemaname = 'public' AND tablename = 'record_collaborator'
     ORDER BY indexname`);
  console.log('\n=== public.record_collaborator INDEXES (AFTER) ===');
  console.log(JSON.stringify(idx.rows, null, 2));

  // === VERIFY: table grants (anon/PUBLIC must hold NONE; authenticated holds DML) ===
  const grants = await client.query(
    `SELECT table_name, grantee, privilege_type
     FROM information_schema.role_table_grants
     WHERE table_schema = 'public'
       AND table_name   = 'record_collaborator'
       AND grantee IN ('anon','authenticated','PUBLIC')
     ORDER BY grantee, privilege_type`);
  console.log('\n=== public.record_collaborator TABLE GRANTS (AFTER) ===');
  console.log(JSON.stringify(grants.rows, null, 2));

  client.release();
  await pg.end();
})().catch(e => { console.error('ERR:', e.message); process.exit(1); });
