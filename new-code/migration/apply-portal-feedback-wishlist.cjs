'use strict';
/**
 * ALT-229-ext — Client Portal FEEDBACK + WISHLIST tables.
 * Creates portal.meeting_feedback and portal.wishlist with RLS.
 * RUN AFTER apply-portal-foundation.cjs.
 */
require('dotenv').config();
const { Pool } = require('pg');

const SQL = `
-- portal.meeting_feedback — one row per client feedback submission per meeting
CREATE TABLE IF NOT EXISTS portal.meeting_feedback (
  feedback_id        bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  meeting_id         bigint NOT NULL,
  client_assoc_id    bigint NOT NULL,
  auth_uid           uuid NOT NULL,
  overall_rating     smallint NOT NULL CHECK (overall_rating BETWEEN 1 AND 5),
  rep_rating         smallint CHECK (rep_rating BETWEEN 1 AND 5),
  comments           text DEFAULT NULL,
  wants_follow_up    boolean DEFAULT NULL,
  submitted_at       timestamptz NOT NULL DEFAULT now(),
  created_date       timestamptz NOT NULL DEFAULT now(),
  UNIQUE (meeting_id, auth_uid)
);
CREATE INDEX IF NOT EXISTS mf_meeting_idx ON portal.meeting_feedback (meeting_id);
CREATE INDEX IF NOT EXISTS mf_client_idx  ON portal.meeting_feedback (client_assoc_id);
CREATE INDEX IF NOT EXISTS mf_uid_idx     ON portal.meeting_feedback (auth_uid);

-- Enable RLS
ALTER TABLE portal.meeting_feedback ENABLE ROW LEVEL SECURITY;

-- Allow authenticated users to INSERT/SELECT their own rows
DROP POLICY IF EXISTS feedback_own ON portal.meeting_feedback;
CREATE POLICY feedback_own ON portal.meeting_feedback
  FOR ALL TO authenticated
  USING (auth_uid = auth.uid())
  WITH CHECK (auth_uid = auth.uid());

-- portal.wishlist — company request from portal users
CREATE TABLE IF NOT EXISTS portal.wishlist (
  wishlist_id        bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  client_assoc_id    bigint NOT NULL,
  auth_uid           uuid NOT NULL,
  company_name       text NOT NULL,
  notes              text DEFAULT NULL,
  status             text NOT NULL DEFAULT 'Pending'
                     CHECK (status IN ('Pending','In Review','Added','Declined')),
  created_at         timestamptz NOT NULL DEFAULT now(),
  updated_at         timestamptz DEFAULT NULL
);
CREATE INDEX IF NOT EXISTS wl_client_idx ON portal.wishlist (client_assoc_id);
CREATE INDEX IF NOT EXISTS wl_uid_idx    ON portal.wishlist (auth_uid);

ALTER TABLE portal.wishlist ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS wishlist_own ON portal.wishlist;
CREATE POLICY wishlist_own ON portal.wishlist
  FOR ALL TO authenticated
  USING (auth_uid = auth.uid())
  WITH CHECK (auth_uid = auth.uid());

-- Grants to authenticated (INSERT for new requests + SELECT own rows via RLS)
GRANT SELECT, INSERT ON portal.meeting_feedback TO authenticated;
GRANT SELECT, INSERT ON portal.wishlist TO authenticated;
GRANT USAGE, SELECT ON portal.meeting_feedback_feedback_id_seq TO authenticated;
GRANT USAGE, SELECT ON portal.wishlist_wishlist_id_seq TO authenticated;

-- service_role full access
GRANT SELECT, INSERT, UPDATE, DELETE ON portal.meeting_feedback TO service_role;
GRANT SELECT, INSERT, UPDATE, DELETE ON portal.wishlist TO service_role;
`;

(async () => {
  const pg = new Pool({ connectionString: process.env.PG_CONNECTION_STRING });
  const client = await pg.connect();
  try {
    await client.query('BEGIN');
    await client.query(SQL);
    await client.query('COMMIT');
    console.log('APPLIED apply-portal-feedback-wishlist OK (committed).');
  } catch (e) {
    await client.query('ROLLBACK');
    console.error('APPLY FAILED, rolled back:', e.message);
    client.release();
    await pg.end();
    process.exit(1);
  }

  const tbls = await client.query(
    `SELECT table_name FROM information_schema.tables
     WHERE table_schema='portal' AND table_type='BASE TABLE' ORDER BY table_name`);
  console.log('\n=== portal BASE TABLES ===');
  console.log(tbls.rows.map(r => r.table_name).join(', '));

  client.release();
  await pg.end();
})().catch(e => { console.error('ERR:', e.message); process.exit(1); });
